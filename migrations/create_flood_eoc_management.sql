-- Flood EOC Session-based Management + Daily Reporting Cycle
-- Designed to extend existing eoc_sessions / eoc_teams without replacing them.

CREATE TABLE IF NOT EXISTS eoc_reporting_cycles (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  session_id BIGINT NOT NULL,
  report_date DATE NOT NULL,
  day_no INT NOT NULL,
  deadline_at DATETIME NOT NULL,
  cycle_status VARCHAR(40) NOT NULL DEFAULT 'open',
  closed_at DATETIME NULL,
  dashboard_generated_at DATETIME NULL,
  briefing_meeting_at DATETIME NULL,
  created_by BIGINT NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  UNIQUE KEY uniq_cycle_session_date (session_id, report_date),
  KEY idx_cycle_status (cycle_status),
  KEY idx_cycle_deadline (deadline_at)
);

CREATE TABLE IF NOT EXISTS team_daily_inputs (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  session_id BIGINT NOT NULL,
  reporting_cycle_id BIGINT NOT NULL,
  report_date DATE NOT NULL,
  team_code VARCHAR(40) NOT NULL,
  team_name VARCHAR(160) NOT NULL,
  required_data_name VARCHAR(255) NOT NULL,
  input_status VARCHAR(40) NOT NULL DEFAULT 'Not Started',
  completed_percent INT NOT NULL DEFAULT 0,
  deadline_at DATETIME NOT NULL,
  submitted_at DATETIME NULL,
  is_late TINYINT(1) NOT NULL DEFAULT 0,
  submitted_by BIGINT NULL,
  reviewed_by BIGINT NULL,
  reviewed_at DATETIME NULL,
  review_status VARCHAR(40) NOT NULL DEFAULT 'Draft',
  approved_by BIGINT NULL,
  approved_at DATETIME NULL,
  missing_fields JSON NULL,
  remarks TEXT NULL,
  audit_log_id BIGINT NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  KEY idx_team_inputs_cycle (reporting_cycle_id, team_code),
  KEY idx_team_inputs_status (input_status, review_status),
  KEY idx_team_inputs_late (is_late, submitted_at)
);

