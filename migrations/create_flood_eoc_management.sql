CREATE TABLE IF NOT EXISTS flood_reports (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  session_id BIGINT NOT NULL,
  reporting_cycle_id BIGINT NOT NULL,
  report_date DATE NOT NULL,
  district VARCHAR(120) NOT NULL,
  subdistrict VARCHAR(120) NULL,
  village VARCHAR(120) NULL,
  latitude DECIMAL(10,7) NULL,
  longitude DECIMAL(10,7) NULL,
  flood_type VARCHAR(80) NOT NULL,
  water_level_cm INT NULL,
  water_trend VARCHAR(40) NULL,
  severity_level VARCHAR(40) NULL,
  affected_households INT NOT NULL DEFAULT 0,
  affected_population INT NOT NULL DEFAULT 0,
  vulnerable_population INT NOT NULL DEFAULT 0,
  road_affected TINYINT(1) NOT NULL DEFAULT 0,
  health_facility_affected TINYINT(1) NOT NULL DEFAULT 0,
  verification_status VARCHAR(40) NOT NULL DEFAULT 'Draft',
  public_visibility TINYINT(1) NOT NULL DEFAULT 0,
  publish_status VARCHAR(40) NOT NULL DEFAULT 'internal',
  reported_by_team VARCHAR(40) NOT NULL,
  reported_by BIGINT NULL,
  submitted_at DATETIME NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  KEY idx_flood_reports_cycle (reporting_cycle_id),
  KEY idx_flood_reports_area (district, subdistrict, village),
  KEY idx_flood_reports_publish (public_visibility, publish_status)
);

